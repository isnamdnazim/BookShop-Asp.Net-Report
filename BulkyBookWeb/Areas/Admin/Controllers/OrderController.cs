        public async Task<string> RenderViewToStringAsync(string viewName, object model)
        {
            try
            {
                var httpContext = new DefaultHttpContext { RequestServices = HttpContext.RequestServices };
                var actionContext = new ActionContext(httpContext, new Microsoft.AspNetCore.Routing.RouteData(), new Microsoft.AspNetCore.Mvc.Abstractions.ActionDescriptor());
                var viewEngineResult = _viewEngine.FindView(actionContext, viewName, false);
                if (!viewEngineResult.Success)
                {
                    throw new FileNotFoundException($"View not found: {viewName}");
                }
                // var lst = _context.PurchaseOrderDetails.ToList();
                var viewDictionary = new ViewDataDictionary(new EmptyModelMetadataProvider(), new ModelStateDictionary())
                {
                    Model = model
                };
                var tempDataDictionary = new TempDataDictionary(httpContext, _tempDataProvider);
                using var sw = new StringWriter();
                var viewContext = new ViewContext(
                    actionContext,
                    viewEngineResult.View,
                    viewDictionary,
                    tempDataDictionary,
                    sw,
                    new HtmlHelperOptions()
                );
                await viewEngineResult.View.RenderAsync(viewContext);
                return sw.ToString();
            }
            catch (Exception ex) { throw ex; }
        }

        #region API CALLS
        [HttpGet]
        public IActionResult GetAll(string status)
        {
            IEnumerable<OrderHeader> orderHeaders;

            if (User.IsInRole(SD.Role_Admin) || User.IsInRole(SD.Role_Employee))
            {
                orderHeaders = _unitOfWork.OrderHeader.GetAll(includeProperties: "ApplicationUser");
            }
            else
            {
                var claimsIdentity = (ClaimsIdentity)User.Identity;
                var claim = claimsIdentity.FindFirst(ClaimTypes.NameIdentifier);
                orderHeaders = _unitOfWork.OrderHeader.GetAll(u => u.ApplicationUserId == claim.Value, includeProperties: "ApplicationUser");
            }

            switch (status)
            {
                case "pending":
                    orderHeaders = orderHeaders.Where(u => u.PaymentStatus == SD.PaymentStatusDelayedPayment);
                    break;
                case "inprocess":
                    orderHeaders = orderHeaders.Where(u => u.OrderStatus == SD.StatusInProcess);
                    break;
                case "completed":
                    orderHeaders = orderHeaders.Where(u => u.OrderStatus == SD.StatusShipped);
                    break;
                case "approved":
                    orderHeaders = orderHeaders.Where(u => u.OrderStatus == SD.StatusApproved);
                    break;
                default:
                    break;
            }


            return Json(new { data = orderHeaders });
        }
        #endregion
